        <h3 class="title">Setting up your Neon Sign Customize Plugin</h3>
        <br />
        <p>Welcome to&nbsp;<strong>Neon Sign Customize Plugin! </strong>We are glad to have you onboard.</p>
        <p>Using our app gives you the power to have the world's most advanced Neon Sign Customize Plugin on your
            Shopify store.
            You are setting up an advanced custom product builder specifically tailored to the sign industry. </p>
        <p>Our app provides many options and configurations both for an admin and for the customer. Some of the
            terminologies and systems we use throughout the app will most likely require further research, so please
            keep in mind that this is not an app that can go live in a day (though possible if you are already familiar
            with the neon sign industry ).</p>
        <p>To ensure success with our product, it's essential to understand how our app works; please read through these
            instructions and ensure you attend to each item in the order they appear.&nbsp;</p>
        <h4>1. Choosing your sizing and pricing model</h4>
        <p>One of the most important tasks that should be done is deciding which sizing and pricing model you would like
            to set up in your store. Although our <a href="https://sign-product-customiser.myshopify.com/">two demos</a>
            may look the same on initial impression, the way they calculate the pricing and sizing is entirely
            different, and as such, both have two unique approaches to how you would work with your suppliers.&nbsp;</p>
        <p>If you need help deciding which one to use, we recommend the <strong>simple model </strong>when initially
            using our app, as the admin has been designed to make setting up the sizing and pricing as simple as
            possible. You can always move to the advanced model once our app has been running your store for some time.
        </p>
        <p>Our <strong>advanced model </strong>provides<strong>&nbsp;the best customer experience </strong>due to its
            flexibility and accuracy, which require a more complex setup; we recommend the advanced model&nbsp;for any
            business that builds custom signs or has experience in the sign industry.</p>
        <h4>2. How to find the proper sign manufacturer - (important for drop shippers)</h4>
        <p>We have created a list of manufacturers who use and understand our app deeply. You can find this list by
            clicking the&nbsp;<strong>Manufacturers </strong>button in the top right menu of our app's admin interface.
            From there, you can contact the manufacturers and ask for the necessary pricing information you will need to
            set up your Neon Sign Customize Plugin; you can also&nbsp;easily select manufacturers that service the same
            regions
            that your business operates in. Of course, you can find and utilise any manufacturer that's not on this list
            for building custom signs, we have created a step-by-step process to help ask the right questions and get
            setup as soon as possible.</p>
        <p>We aim to continue building upon this list; if you are a manufacturer, please contact us so that we can
            include you on the list.&nbsp;</p>
